    private void ModificarRecursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarRecursoActionPerformed
        if((NuevoNombreTexto.getText().trim().equals(""))||(RecursosCreados1.getSelectedIndex()==-1)) {
            area.setText("Error al modificar recurso. Hay algún campo vacío");
        }
        else {
            Recurso rec = registro2.getListaRecursos().get(RecursosCreados1.getSelectedIndex());
            rec.setNombre(NuevoNombreTexto.getText());
            RecursosCreados1.removeAllItems();
            RecursosCreados2.removeAllItems();
            for (Recurso rec2: registro2.getListaRecursos()){
                RecursosCreados1.addItem(rec2.getNombre() + " " + rec2.getID());
                RecursosCreados2.addItem(rec2.getNombre() + " " + rec2.getID());
            }
            area.setText("Recurso modificado con éxito" + "\n" + rec.toString());
            NuevoNombreTexto.setText("");
        }
    }//GEN-LAST:event_ModificarRecursoActionPerformed

    private void EliminarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarBotonActionPerformed
        if(RecursosCreados2.getSelectedIndex()==-1) {
            area.setText("Error al eliminar recurso. Hay algún campo vacío");
        }
        else {
            Recurso rec = registro2.getListaRecursos().get(RecursosCreados2.getSelectedIndex());
            if (!(rec.getEnUso())){
                registro2.EliminarRecurso(rec);
                RecursosCreados1.removeAllItems();
                RecursosCreados2.removeAllItems();
                for (Recurso rec2: registro2.getListaRecursos()){
                    RecursosCreados1.addItem(rec2.getNombre() + " " + rec2.getID());
                    RecursosCreados2.addItem(rec2.getNombre() + " " + rec2.getID());
                }
                area.setText("Recurso eliminado con éxito");
            } else{
                area.setText("El recurso está en uso por un personaje, devuélvalo antes");
            }
        }
    }//GEN-LAST:event_EliminarBotonActionPerformed

