    private void CrearRecursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearRecursoActionPerformed
        if((NombreTexto.getText().trim().equals(""))||(TipoRecurso.getSelectedIndex()==-1)) {
            area.setText("Error al crear recurso. Hay algún campo vacío");
        }
        else {
            Recurso rec=null;
            switch(TipoRecurso.getSelectedIndex()) {
                case 0: {
                    int manejo=0;
                    int rango=0;
                    if(UnaMano.isSelected()){manejo=1;}
                    else if(DosManos.isSelected()){manejo=2;}
                    if(cuerpo.isSelected()){rango=0;}
                    else if(distancia.isSelected()){rango=(int)(Math.random()*9+1);}
                    rec = new Arma(NombreTexto.getText(),registro2,rango,manejo); 
                } break;
                case 1: {
                    rec = new Hechizo( NombreTexto.getText(),registro2);
                } break;
                case 2: {
                    rec = new Conjuro(NombreTexto.getText(),registro2);
                } break;
            }
            RecursosCreados1.addItem(rec.getNombre() + " " + rec.getID());
            RecursosCreados2.addItem(rec.getNombre() + " " + rec.getID());
            area.setText("Recurso creado con éxito" + "\n" + rec.toString());
            NombreTexto.setText("");
            panelarma.setVisible(false);
            elegirManejo.clearSelection();
            elegirRango.clearSelection();

            try {
                TipoRecurso.setSelectedIndex(-1);
            } catch(java.lang.NullPointerException e) {

            }
        }
    }//GEN-LAST:event_CrearRecursoActionPerformed

